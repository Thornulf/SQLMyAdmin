    private void jListTablesMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListTablesMouseReleased
        String lsDataBase = jListBaseDonnees.getSelectedValue();
        String lsTable = jListTables.getSelectedValue();

        ResultSet rs;

        try {
            Statement stmt = cn.createStatement();
            rs = stmt.executeQuery("SELECT * FROM " + lsDataBase + "." + lsTable);
            jTable.setModel(GlobalesFonctions.fillTableResult(rs));

        } catch (SQLException e) {
            //jLabelMessageErreur.setText(e.getMessage());
        }
    }//GEN-LAST:event_jListTablesMouseReleased

    private void jButtonSupLigneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSupLigneActionPerformed
        String id = jTable.getColumnName(0);
        String values = jTable.getValueAt(jTable.getSelectedRow(), 0).toString();
        
        String lsDataBase = jListBaseDonnees.getSelectedValue();
        String lsTable = jListTables.getSelectedValue();
        
        String sqlDelete = "DELETE FROM " + lsDataBase + "." + lsTable + " WHERE " + id + " = '" + values + "'";

        try (PreparedStatement lpst = cn.prepareStatement(sqlDelete)) {
            int affect = lpst.executeUpdate();
            if (affect >= 1) {
                jLabelMessage.setText("La ligne est supprim√©");
            } else {
                jLabelMessage.setText("aucune suppression effectu√©e");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        
        jListTablesMouseReleased(null);
    }//GEN-LAST:event_jButtonSupLigneActionPerformed


